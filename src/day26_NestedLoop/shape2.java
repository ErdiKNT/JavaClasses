package day26_NestedLoop;

public class shape2 {
    public static void main(String[] args) {
        for (int i = 0; i < 8; i++) {
            System.out.println(" * ");

            for (int j = 0; j < 2; j++) {
                System.out.print(" * ");
            }

        }
    }
}
