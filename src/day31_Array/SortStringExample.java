package day31_Array;

import java.util.Arrays;

public class SortStringExample {
    public static void main(String[] args) {
        String[] arr = {"java", "Hello", "$Dollar", " 4four", "five5","Zebra", "SIX", "6six"};
        Arrays.sort(arr);
        System.out.println(Arrays.toString(arr)); // [$Dollar, 4four, Hello, SIX, Zebra, five5, java, six]









    }
}
