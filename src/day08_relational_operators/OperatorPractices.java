package day08_relational_operators;

public class OperatorPractices {
    public static void main(String[] args) {
        int b = 10; //b = 10
        b++; //11 post increment, no other code, adds 1.
        System.out.println(b);
        b--;//10 post decrement, no other code, minus 1.
        System.out.println(b); // print the value of b, which is 10.
        System.out.println(b++);//it will be 10 also, because there is a print statement code.
        System.out.println(b++);//it will add 1 in here, due to previous line post increment.











    }
}
