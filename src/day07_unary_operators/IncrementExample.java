package day07_unary_operators;

public class IncrementExample {
    public static void main(String[] args) {
        int x = 0;
        System.out.println(x);//0

        x = x+1;
        System.out.println(x);//1

        x++;
        System.out.println(x);//2

        ++x;
        System.out.println(x);//3  In this line it will be in next step already.
        System.out.println(x++);//3 it will run the statement x first. Then it will add +1 to x.
        System.out.println(x);// In thi line x will be added +1.









    }
}
