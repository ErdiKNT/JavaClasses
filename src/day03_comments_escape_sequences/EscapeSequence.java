package day03_comments_escape_sequences;

public class EscapeSequence {
    public static void main(String[] args) {
        // \" This symbol basically puts quotes to the specific word or words.
        System.out.println("I love \"java\" programming");
        System.out.println("\"This is a quote\"");
        // If we want to put backslash just type \\. The one for is syntax(Grammar) another one is to be typed.
        System.out.println("\\");
        System.out.println("Erdi\\Fadime\\Rukiye\\Eren\\betul\\Sude\\Zeki\\Aydin\\Ayla\\Necla");
        System.out.println("\"/\"");



    }
}
