package day22_Loops;

public class Letters {
    public static void main(String[] args) {
        char letters = 'a';
        while (letters <= 'z') {
            System.out.print(letters + " ");
            letters++;
        }

        System.out.println();

        char letter = 'z';
        while(letter>='a'){
            System.out.print(letter + " ");
            letter--;
        }






    }
}
